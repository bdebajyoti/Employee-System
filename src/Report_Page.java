    private void count()
    {
        x=0;
        String sql="select count(*) from employee";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                x=x+rs.getInt("count(*)");
            }
            cont.setText(""+x);
        }catch(Exception ex){
            System.err.println(ex);
        }
    }
    private void filltab()
    {
        String sql="select empid,fname,lname,contact,bankname from employee";
        try
        {
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery(sql);
            tab.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception ex){
            System.err.println(ex);
        }
    }
